# Tipos de datos primitivos

# Numeros enteros
# Numeros de punto flotante
# Texto - string - Cadenas de caracteres
# Booleanos

#_-_-_-__-_-_-_-_-

nombre = "Paco"
print(nombre)
# > Paco
nombre2 = 'juan'
print(nombre2)
# > juan
# Todo dentro de comillas es un texto - string

# concatenando (unir texto)
print(nombre + nombre2)
# > Pacojuan

# multiplicar texto por un numero
print(nombre * 2)
# > PacoPaco

# concatenando mas texto(unir texto)
print(nombre + ", " + nombre2)
# > Paco, juan

# floats
numero_decimal = 3.4
print(numero_decimal)
# >3.4

# Booleanos
es_estudiante = True
print(es_estudiante)
# >True
trabaja = False
print(trabaja)
# >False


